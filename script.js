
function darkMode(){
    document.getElementById('darkThemeActive').style.background='rgb(52, 38, 65)';
    var h2=document.getElementById("darkModeText");
    h2.style.color='white';

}
function lightMode(){
    document.getElementById('darkThemeActive').style.background='white';
    document.getElementById("darkModeText").style.color= 'black';

}



